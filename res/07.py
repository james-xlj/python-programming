pen.down()
pen.fillcolor('red')
pen.begin_fill()
pen.left(45)
pen.forward(120)
pen.circle(60,180)
pen.right(90)
pen.circle(60,180)
pen.forward(120)
pen.left(45)
pen.end_fill()
pen.up()
pen.goto(0,0)
pen.down()
pen.begin_fill()
pen.left(45)
pen.forward(120)
pen.circle(60,180)
pen.right(90)
pen.circle(60,180)
pen.forward(120)
pen.end_fill()
